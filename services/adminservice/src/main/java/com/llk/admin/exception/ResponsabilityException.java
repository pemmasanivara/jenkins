package com.llk.admin.exception;

public class ResponsabilityException extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = 7075299274582268043L;
	
	public ResponsabilityException() {
		super();
	}

	public ResponsabilityException(String message) {
		super(message);
	}

	public ResponsabilityException(String message, Throwable t) {
		super(message, t);
	}

}
